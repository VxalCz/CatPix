/**
 * Render text onto an ImageData at pixel position (x, y).
 * Returns a new ImageData with the text drawn.
 */
export function renderText(
  text: string,
  imageData: ImageData,
  x: number,
  y: number,
  color: string,
): ImageData {
  const result = new ImageData(new Uint8ClampedArray(imageData.data), imageData.width, imageData.height)
  const [r, g, b, a] = hexToRgba(color)
  const { width, height } = result
  const dst = result.data

  let curX = x
  for (const ch of text) {
    const code = ch.charCodeAt(0)
    const idx = code - FIRST_CHAR
    const glyphData = idx >= 0 && idx < FONT_DATA.length ? FONT_DATA[idx] : FONT_DATA[0]

    for (let row = 0; row < CHAR_H; row++) {
      const rowBits = glyphData[row]
      for (let col = 0; col < CHAR_W; col++) {
        if (rowBits & (1 << (CHAR_W - 1 - col))) {
          const px = curX + col
          const py = y + row
          if (px >= 0 && px < width && py >= 0 && py < height) {
            const dstIdx = (py * width + px) * 4
            dst[dstIdx] = r
            dst[dstIdx + 1] = g
            dst[dstIdx + 2] = b
            dst[dstIdx + 3] = a
          }
        }
      }
    }

    curX += CHAR_W + 1  // 1px kerning gap
  }

  return result
}

